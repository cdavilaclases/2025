    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        
        if(modificarUsuario!=null){
            modificarUsuario.nombre = jTextField1.getText();
            modificarUsuario.password = jTextField2.getText();
            modificarUsuario.rol = jComboBox1.getSelectedItem().toString();
            JOptionPane.showMessageDialog(this, "Usuario modificado.");
            llenarTabla();
            modificarUsuario = null;
            jTextField1.setText("");
            jTextField2.setText("");
        }else{
            Usuario nuevo = new Usuario();
            nuevo.nombre = jTextField1.getText();
            nuevo.password = jTextField2.getText();
            nuevo.rol = jComboBox1.getSelectedItem().toString();
            ProyectoA2026.usuarios.add(nuevo);
            JOptionPane.showMessageDialog(this, "Usuario Creado.");
            llenarTabla();
            jTextField1.setText("");
            jTextField2.setText("");
        }
        
    }//GEN-LAST:event_jButton4ActionPerformed

    

